/*
  Trust bar data can arrive from more than one source depending on where
  ServicePageContent is rendered from (the production page vs. a CMS/editor
  preview), and those sources don't always agree on field names or on
  whether an `id` is present at all. Rather than forcing every caller to
  conform to the strict TrustBarItem shape from lib/services.ts, accept
  this looser shape here and normalize it below before it reaches <TrustBar>.
*/
export type LooseTrustBarItem = {
  id?: string;
  label?: string;
  title?: string;
  value?: string;
  [key: string]: unknown;
};

