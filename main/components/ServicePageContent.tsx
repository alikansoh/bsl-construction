function hasValue(value?: string | null): value is string {
  return typeof value === "string" && value.trim().length > 0;
}

/*
  Never give Next Link an empty string. Empty links can cause navigation
  problems and do not have meaningful behavior in the editor preview.
*/
function safeHref(href?: string | null, fallback = "#quote") {
  return hasValue(href) ? href.trim() : fallback;
}

/*
  Normalizes whatever loose trust-bar data a caller passes in (CMS/editor
  config, hardcoded arrays, etc.) into the strict TrustBarItem[] shape
  <TrustBar> expects. Falls back through label -> title -> value for the
  display text, and generates a stable id from the index when one isn't
  provided. Items with no usable text are dropped.

  NOTE: <TrustBar>'s icon lookup is keyed by `id` (e.g. "insured", "team",
  "guarantee", "service", "recommended"). If the source data doesn't supply
  one of those ids, TrustBar falls back to its default icon for that item —
  that's a display detail to fix at the data source, not something this
  normalizer can guess correctly on its own.
*/
function normalizeTrustBarItems(
  items: LooseTrustBarItem[] | undefined,
): TrustBarItem[] {
  if (!items?.length) return [];

  return items
    .map((item, index) => {
      const label = item.label ?? item.title ?? item.value ?? "";

      return {
        id: hasValue(item.id) ? item.id!.trim() : `trust-${index}`,
        label,
      };
    })
    .filter((item): item is TrustBarItem => hasValue(item.label));
}

/*
  This renders the exact HTML emitted by RichTextEditor.

  IMPORTANT:
  Sanitize rich text in your API before persistence. Do not rely on this
  renderer as an HTML sanitizer.
*/
function RichText({
  html,
  fallback,
  className = "",
}: {
  html?: string | null;
  fallback?: string;
  className?: string;
}) {
  if (!hasValue(html)) {
    return fallback ? <p>{fallback}</p> : null;
  }

  return (
    <div
      className={className}
      dangerouslySetInnerHTML={{ __html: html }}
    />
  );
}

